// ── Animated stroke icon ──────────────────────────────────────────────────────
function StrokeIcon({
  paths,
  color = "#FFFFFF",
  revealed,
  iconIndex,
}: {
  paths: string[];
  color?: string;
  revealed: boolean;
  iconIndex: number;
}) {
  const iconDelay = iconIndex * ICON_STAGGER;

  const pathStyle = (pathIndex: number): React.CSSProperties => ({
    strokeDasharray: 1,
    strokeDashoffset: revealed ? 0 : 2,
    transition: revealed
      ? `stroke-dashoffset ${PATH_DURATION}s cubic-bezier(0.4,0,0.2,1) ${(iconDelay + pathIndex * PATH_STAGGER).toFixed(3)}s`
      : "none",
  });

  return (
    <svg
      xmlns="http://www.w3.org/2000/svg"
      viewBox="0 0 24 24"
      aria-hidden="true"
      style={{ width: "100%", height: "100%", display: "block" }}
      fill="none"
      stroke={color}
      strokeWidth={1}
      strokeLinecap="butt"
      strokeLinejoin="round"
    >
      {paths.map((d, i) => (
        <path key={i} d={d} pathLength={1} style={pathStyle(i)} />
      ))}
    </svg>
  );
}

// ── Main export ───────────────────────────────────────────────────────────────
export function ServicesGrid() {
  const sectionRef = useRef<HTMLElement>(null);
  const [revealed, setRevealed] = useState(false);

  useEffect(() => {
    const el = sectionRef.current;
    if (!el) return;

    const observer = new IntersectionObserver(
      ([entry]) => {
        if (entry.isIntersecting) {
          setRevealed(true);
          observer.disconnect();
        }
      },
      { threshold: 0.25 }
    );

    observer.observe(el);
    return () => observer.disconnect();
  }, []);

  return (
    <section
      ref={sectionRef}
      id="services"
      className="py-20 md:py-[80px]"
      style={{ backgroundColor: DARK }}
    >
      <div className="max-w-[1200px] mx-auto px-6">
        {/* Label */}
        <p
          className="mb-4 text-xs font-medium uppercase tracking-[2px]"
          style={{ color: BLUE }}
        >
          Our Services
        </p>

        {/* Headline */}
        <h2
          className="mb-12 text-[36px] leading-[1.1] tracking-[-3px] md:text-[60px]"
          style={{ color: "#FFFFFF" }}
        >
          How We Do It
        </h2>

        {/* 4 × 2 grid */}
        <div className="grid grid-cols-2 gap-6 sm:grid-cols-4">
          {SERVICES.map(({ label, href, paths }, i) => (
            <a
              key={label}
              href={href}
              className="group flex flex-col items-center gap-4 rounded-xl p-6 text-center transition-colors duration-200 hover:bg-[#00A1E1]"
            >
              <div style={{ width: 56, height: 56 }}>
                <StrokeIcon paths={paths} color="#FFFFFF" revealed={revealed} iconIndex={i} />
              </div>

              <span
                className="text-sm font-medium leading-snug md:text-base"
                style={{ color: "#FFFFFF" }}
              >
                {label}
              </span>
            </a>
          ))}
        </div>
      </div>
    </section>
  );
}
